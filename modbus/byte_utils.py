# -*- coding: utf-8 -*-


# [to_u16] #############################################################################################################

def to_u16(chars):

    if len(chars) != 2:
        raise Exception('Length should be 2')

    return ord(chars[0]) * 256 + ord(chars[1])


# [to_u8] ##############################################################################################################

def to_u8(chars):

    if len(chars) != 1:
        raise Exception('Length should be 1')

    return ord(chars[0])


# [from_u16] ###########################################################################################################

def from_u16(value):

    return chr(value >> 8), chr(value & 0x00FF)


# [from_u8] ############################################################################################################

def from_u8(value):

    return chr(value)